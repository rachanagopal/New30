package javabasics;

public class Loopsconcept {

	public static void main(String[] args) {

		/*
		 * System.out.println(" 1"); System.out.println(" 2");
		 */

		// For Loop
			  
		  int i; 
		  
		  for (i=0; i <= 10; i++ ) { 
			  System.out.println(i);
			  }
		   
		  System.out.println("==================");
			
		// While loop

	        int j =0 ;
			while  (j <= 10) {
			System.out.println(j);
			j++;

		}
			
			System.out.println("==================");
	// print from 10-0
			
			for (int k=10;k>=-10;k--) {
				System.out.println(k);
			}
			
			

	}
	
	
	


}
